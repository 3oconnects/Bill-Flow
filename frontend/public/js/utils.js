// ── TOAST ─────────────────────────────────────
function toast(msg, type = 'success') {
  const t = document.createElement('div');
  t.className = `toast ${type}`;
  t.innerHTML = `${type === 'success' ? ICONS.check : ICONS.info}<span>${esc(msg)}</span>`;
  const c = document.getElementById('toast-container');
  c.appendChild(t);
  setTimeout(() => {
    t.style.animation = 'fadeOut .3s ease forwards';
    setTimeout(() => t.remove(), 300);
  }, 3000);
}

// ── CONFIRM ───────────────────────────────────
function confirmDel(msg, cb) { if (confirm(msg || 'Delete this item? This cannot be undone.')) cb(); }

// ── SELECT HELPERS ────────────────────────────
function selectOpts(arr, valKey, labelKey, selected = '') {
  return arr.map(x => `<option value="${esc(String(x[valKey]))}"${String(x[valKey]) === String(selected) ? ' selected' : ''}>${esc(x[labelKey])}</option>`).join('');
}

// ── NUMBER HELPERS ────────────────────────────
const safeNum = v => isNaN(parseFloat(v)) ? 0 : parseFloat(v);

// ── CALC LINE ITEM ────────────────────────────
function calcLine(qty, price, taxRate) {
  const base = safeNum(qty) * safeNum(price);
  const tax  = base * safeNum(taxRate) / 100;
  return { base, tax, total: base + tax };
}
